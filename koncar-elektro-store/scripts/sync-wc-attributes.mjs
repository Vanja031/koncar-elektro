/**
 * Export WC attributes snapshot for filter UI (read-only, run locally).
 * Usage: npm run sync:wc-attributes
 *
 * Reads WP URL + keys from .env (NEXT_PUBLIC_* or VITE_*).
 * Paginates attribute terms (some taxonomies have 1000+ values).
 */
import { writeFileSync, readFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const envPath = resolve(__dirname, '../.env');

function loadEnv() {
  try {
    const content = readFileSync(envPath, 'utf8');
    for (const line of content.split(/\r?\n/)) {
      const m = line.match(/^\s*([^#=]+)=(.*)$/);
      if (m) process.env[m[1].trim()] = m[2].trim().replace(/^["']|["']$/g, '');
    }
  } catch {
    console.warn('No .env file — set NEXT_PUBLIC_WC_CONSUMER_KEY/SECRET');
  }
}

loadEnv();

const BASE =
  process.env.NEXT_PUBLIC_WP_API_URL ||
  process.env.VITE_WP_API_URL ||
  'https://koncarelektro.rs/wp-json';
const CK = process.env.NEXT_PUBLIC_WC_CONSUMER_KEY || process.env.VITE_WC_CONSUMER_KEY || '';
const CS =
  process.env.NEXT_PUBLIC_WC_CONSUMER_SECRET || process.env.VITE_WC_CONSUMER_SECRET || '';

if (!CK || !CS) {
  console.error('Missing WC consumer key/secret in .env');
  process.exit(1);
}

async function fetchJson(path) {
  const url = `${BASE.replace(/\/$/, '')}/wc/v3${path}`;
  const auth = Buffer.from(`${CK}:${CS}`).toString('base64');
  const res = await fetch(url, {
    headers: { Authorization: `Basic ${auth}`, Accept: 'application/json' },
  });
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return { data: await res.json(), headers: res.headers };
}

async function fetchAllPages(pathPrefix) {
  const all = [];
  let page = 1;
  let totalPages = 1;
  while (page <= totalPages) {
    const sep = pathPrefix.includes('?') ? '&' : '?';
    const { data, headers } = await fetchJson(
      `${pathPrefix}${sep}per_page=100&page=${page}`,
    );
    if (!Array.isArray(data)) throw new Error(`Expected array from ${pathPrefix}`);
    all.push(...data);
    totalPages = Number(headers.get('X-WP-TotalPages') || 1);
    page += 1;
  }
  return all;
}

console.log(`Syncing attributes from ${BASE}`);

const attributes = await fetchAllPages('/products/attributes');
const withTerms = [];

for (const attr of attributes) {
  const terms = await fetchAllPages(`/products/attributes/${attr.id}/terms`);
  withTerms.push({
    id: attr.id,
    name: attr.name,
    slug: attr.slug,
    terms: terms.map((t) => ({ id: t.id, name: t.name, slug: t.slug })),
  });
  console.log(`  ${attr.slug} — ${terms.length} terms`);
}

withTerms.sort((a, b) => a.name.localeCompare(b.name, 'sr'));

const out = `/** Auto-generated by scripts/sync-wc-attributes.mjs — do not edit manually */
export const wcAttributes = ${JSON.stringify(withTerms, null, 2)} as const;
`;

writeFileSync(resolve(__dirname, '../src/data/wcAttributes.ts'), out);
console.log(`Wrote ${withTerms.length} attributes to src/data/wcAttributes.ts`);
