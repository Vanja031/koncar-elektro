/**
 * Export WC attributes snapshot for filter UI (read-only, run locally).
 * Usage: npm run sync:wc-attributes
 */
import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const envPath = resolve(__dirname, '../.env');

async function loadEnv() {
  const { readFileSync } = await import('node:fs');
  try {
    const content = readFileSync(envPath, 'utf8');
    for (const line of content.split('\n')) {
      const m = line.match(/^\s*([^#=]+)=(.*)$/);
      if (m) process.env[m[1].trim()] = m[2].trim();
    }
  } catch {
    console.warn('No .env file — set VITE_WC_CONSUMER_KEY/SECRET');
  }
}

const BASE = process.env.VITE_WP_API_URL ?? 'https://koncarelektro.rs/wp-json';
const CK = process.env.VITE_WC_CONSUMER_KEY ?? '';
const CS = process.env.VITE_WC_CONSUMER_SECRET ?? '';

async function fetchJson(path) {
  const url = `${BASE.replace(/\/$/, '')}/wc/v3${path}`;
  const auth = Buffer.from(`${CK}:${CS}`).toString('base64');
  const res = await fetch(url, { headers: { Authorization: `Basic ${auth}`, Accept: 'application/json' } });
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.json();
}

await loadEnv();

const attributes = await fetchJson('/products/attributes?per_page=100');
const withTerms = await Promise.all(
  attributes.map(async (attr) => ({
    id: attr.id,
    name: attr.name,
    slug: attr.slug,
    terms: (await fetchJson(`/products/attributes/${attr.id}/terms?per_page=100`)).map(
      (t) => ({ id: t.id, name: t.name, slug: t.slug }),
    ),
  })),
);

const out = `/** Auto-generated by scripts/sync-wc-attributes.mjs — do not edit manually */\nexport const wcAttributes = ${JSON.stringify(withTerms, null, 2)} as const;\n`;

writeFileSync(resolve(__dirname, '../src/data/wcAttributes.ts'), out);
console.log(`Wrote ${withTerms.length} attributes to src/data/wcAttributes.ts`);
